public class ReflectUtils {
    /**
     *      Java.lang.reflect.Class类
     * getName:获取全类名
     * getSimpleName:获取简单类名
     * getDeclaredFields:获取本类种所有属性
     * getMethods:获取所有public修饰的方法,包含本类及父类的
     * getDeclaredMethods:获取本类所有方法
     * getConstructors:获取所有public修饰的构造器,包含本类及父类的
     * getDeclaredConstructors:获取本类所有构造器
     * getPackage:以Package形式返回包信息
     * getSuperClass:以Class形式返回父类信息
     * getInterfaces:以Class[]形式返回接口信息
     * getAnnotations:以Annotation[]形式返回注解信息
     *
     *      Java.lang.reflect.Field类
     * getModifiers:以int形式  返回修饰符   默认修饰符是0 public:1 private:2 protect:4  static:8 final:16
     * getType:以Class形式返回类型
     * getName:返回属性名
     *
     *
     *      Java.lang.reflect.Method类
     * getModifiers:以int形式返回修饰符
     * getReturnType:以Class形式返回类型
     * getName:返回方法名
     * getParParameterTypes:以Class[]返回参数类型数组
     *
     *
     *      Java.lang.reflect.Constructor类
     * getModifiers:以int形式返回修饰符
     * getName:返回构造器名(全类名)
     * getParameterTypes:以Class[]返回参数类型数组
     *
     */
}
